  let score = 60;
  
  // æ ¹æ®è®­ç»ƒæ¬¡æ•°å¢åŠ åˆ†æ•°ï¼Œæœ€å¤šå¢åŠ 20åˆ†
  const sessionScore = Math.min(trainingStore.stats.totalSessions / 10 * 20, 20);
  
